def t(key: str, **kwargs) -> str:
    """Translates a key into the active language."""
    try:
        lang = st.session_state.get("lang", "es")
    except Exception:
        lang = "es"
        
    if lang not in TRANSLATIONS:
        lang = "es"
        
    text = TRANSLATIONS[lang].get(key, f"[{key}]")
    if kwargs:
        try:
            return text.format(**kwargs)
        except Exception:
            return text
    return text
